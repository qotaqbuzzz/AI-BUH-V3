import type { ScoreResult, Verdict } from "./scorer.js";

const EMOJI: Record<Verdict, string> = {
  pass:         "✅",
  fail:         "❌",
  needs_review: "⚠️",
  skip:         "⏭️",
};

export function generateReport(results: ScoreResult[], runDate: string): string {
  const total    = results.length;
  const passes   = results.filter((r) => r.verdict === "pass").length;
  const fails    = results.filter((r) => r.verdict === "fail").length;
  const reviews  = results.filter((r) => r.verdict === "needs_review").length;
  const skips    = results.filter((r) => r.verdict === "skip").length;
  const passRate = total > 0 ? ((passes / total) * 100).toFixed(1) : "0.0";
  const avgMs    = total > 0
    ? Math.round(results.reduce((s, r) => s + r.durationMs, 0) / total)
    : 0;

  const domainMap = new Map<string, ScoreResult[]>();
  for (const r of results) {
    const arr = domainMap.get(r.domain) ?? [];
    arr.push(r);
    domainMap.set(r.domain, arr);
  }

  const lines: string[] = [
    `# Eval Report — ${runDate}`,
    "",
    `**Pass rate:** ${passes}/${total} (${passRate}%)  `,
    `**Breakdown:** ✅ ${passes} pass · ❌ ${fails} fail · ⚠️ ${reviews} needs_review · ⏭️ ${skips} skip  `,
    `**Avg latency:** ${avgMs}ms`,
    "",
    "---",
    "",
  ];

  for (const [domain, domainResults] of domainMap) {
    const dp = domainResults.filter((r) => r.verdict === "pass").length;
    lines.push(`## ${domain} (${dp}/${domainResults.length})`);
    lines.push("");
    lines.push("| ID | Question | Verdict | Reason | ms |");
    lines.push("|----|---------|---------|---------|----|");
    for (const r of domainResults) {
      const q      = r.question.length > 60 ? r.question.slice(0, 57) + "…" : r.question;
      const reason = r.reason.length > 80 ? r.reason.slice(0, 77) + "…" : r.reason;
      lines.push(`| ${r.id} | ${q} | ${EMOJI[r.verdict]} ${r.verdict} | ${reason} | ${r.durationMs} |`);
      if (r.note) lines.push(`| | | | _${r.note}_ | |`);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push(`_Generated by \`npm run eval\` on ${runDate}_`);

  return lines.join("\n");
}
